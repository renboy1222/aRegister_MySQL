    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (this.title.equals("New")) {
            int response = JOptionPane.showConfirmDialog(jFrameRegister, "Are you sure to save " + jTextFieldCitizenship.getText() + " ?", "Save confirmation", JOptionPane.YES_NO_OPTION);
            if (response == JOptionPane.YES_OPTION) {
                this.citizenship.setId(null);
                this.citizenship.setCitizenship(jTextFieldCitizenship.getText());
                citizenshipDAOImpl.addCitizenship(citizenship);
                this.dispose();
            }
        } else if (this.title.equals("Update")) {
            int response = JOptionPane.showConfirmDialog(jFrameRegister, "Are you sure to update " + jTextFieldCitizenship.getText() + " ?", "Save confirmation", JOptionPane.YES_NO_OPTION);
            if (response == JOptionPane.YES_OPTION) {
                this.citizenship.setCitizenship(jTextFieldCitizenship.getText());
                citizenshipDAOImpl.updateCitizenship(citizenship);
                this.dispose();
            }
        } else if (this.title.equals("Delete")) {
            int response = JOptionPane.showConfirmDialog(jFrameRegister, "Are you sure to delete " + jTextFieldCitizenship.getText() + " ?", "Save confirmation", JOptionPane.YES_NO_OPTION);
            if (response == JOptionPane.YES_OPTION) {
                citizenshipDAOImpl.deleteCitizenship(citizenship);
                this.dispose();
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed


